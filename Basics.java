package LocalRepo;

public class Basics {
        public static void main(String[] args) {
             //////////  VARIABLE  ////////    
            int a = 20;
            int b = 10;
            int sum = a + b;
            System.out.println(sum);
            int mul = a * b;
            System.out.println(mul);
    
            ////////// LITERALS ////////
            byte age1 = 34;
            int age2 = 56;
            char ch = 'A';
            float f1 = 5.1f;
            double d1 = 4.66D;
            System.out.println(age1);
            System.out.println(age2);
            System.out.println(ch);
            System.out.println(f1);
            System.out.println(d1);
        }
    }
